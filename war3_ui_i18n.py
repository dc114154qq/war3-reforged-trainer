_PUNCTUATION_TRANSLATIONS = str.maketrans(
    {
        "，": ", ",
        "；": "; ",
        "：": ": ",
        "。": ".",
        "？": "?",
        "（": "(",
        "）": ")",
        "“": '"',
        "”": '"',
    }
)


_EN_REPLACEMENTS = tuple(
    sorted(_EN_TRANSLATIONS.items(), key=lambda item: len(item[0]), reverse=True)
)


def translate_ui_text(text: object, language: str) -> str:
    """Translate display text without changing any trainer data or keys."""

    source = "" if text is None else str(text)
    if language != "en" or not source:
        return source
    exact = _EN_TRANSLATIONS.get(source)
    if exact is not None:
        return exact
    translated = source
    for chinese, english in _EN_REPLACEMENTS:
        if chinese in translated:
            translated = translated.replace(chinese, english)
    return translated.translate(_PUNCTUATION_TRANSLATIONS)
