def detect_ui_language(locale_name: str | None = None) -> str:
    """Return the initial UI language from the Windows user locale."""

    if locale_name is None and sys.platform == "win32":
        try:
            buffer = ctypes.create_unicode_buffer(85)
            kernel32 = ctypes.WinDLL("kernel32", use_last_error=True)
            get_locale_name = kernel32.GetUserDefaultLocaleName
            get_locale_name.argtypes = (ctypes.c_wchar_p, ctypes.c_int)
            get_locale_name.restype = ctypes.c_int
            if get_locale_name(buffer, len(buffer)):
                locale_name = buffer.value
        except (AttributeError, OSError):
            locale_name = None
    if locale_name is None:
        try:
            locale_name = locale.getlocale()[0]
        except (ValueError, TypeError):
            locale_name = None
    normalized = (locale_name or "").replace("_", "-").lower()
    return "zh" if normalized.startswith("zh") else "en"


